'use strict';

const fs = require('node:fs');
const path = require('node:path');

const repoRoot = path.resolve(__dirname, '..');
const sourceRoot = path.join(repoRoot, 'official_player_compat_src');
const outputPath = path.join(repoRoot, 'official_player_compat.js');
const sourceFiles = [
  '00-core.js',
  '05-profile-registry.js',
  '10-built-in-menu.js',
  '15-game-index.js',
  '20-platform-state.js',
  '30-local-save.js',
  '40-platform-api.js',
  '50-binary-parser.js',
  '55-event-compat.js',
  '60-free-time.js',
  '70-storage-trace.js',
  '90-bootstrap.js',
];

function normalizeNewlines(text) {
  return text.replace(/^\uFEFF/, '').replace(/\r\n?/g, '\n');
}

function createBundle() {
  const parts = sourceFiles.map((sourceFile) => {
    const sourcePath = path.join(sourceRoot, sourceFile);
    if (!fs.existsSync(sourcePath)) {
      throw new Error(`Missing compatibility source module: ${sourcePath}`);
    }
    return normalizeNewlines(fs.readFileSync(sourcePath, 'utf8')).trimEnd();
  });

  return [
    '// Generated by scripts/build-official-player-compat.js. Edit official_player_compat_src instead.',
    '(function () {',
    parts.join('\n\n'),
    '})();',
    '',
  ].join('\n');
}

function main() {
  const checkOnly = process.argv.slice(2).includes('--check');
  const content = createBundle();

  if (checkOnly) {
    if (!fs.existsSync(outputPath)) {
      throw new Error(`Generated compatibility bundle is missing: ${outputPath}`);
    }
    const existing = normalizeNewlines(fs.readFileSync(outputPath, 'utf8'));
    if (existing !== content) {
      throw new Error(
        'official_player_compat.js is stale. Run node scripts/build-official-player-compat.js',
      );
    }
    console.log('official_player_compat.js is up to date');
    return;
  }

  fs.writeFileSync(outputPath, content, 'utf8');
  console.log(`Generated: ${outputPath}`);
}

try {
  main();
} catch (error) {
  console.error(error.message);
  process.exitCode = 1;
}
